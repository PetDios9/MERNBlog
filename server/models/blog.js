import mongoose from 'mongoose'
const Schema = mongoose.Schema

const blogSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    snippet: {
        type: String,
        required: true
    },
    body: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
    
}, {timestamps: true})

const Blog = mongoose.model('blog', blogSchema)

export default Blog